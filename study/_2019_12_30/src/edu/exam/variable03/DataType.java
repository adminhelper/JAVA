package edu.exam.variable03;

/*변수를 만드는 예약어를
자료형이라고 한다

가장 많이 사용하는 자료형은
int     : 정수
double  : 실수(소수점)
char    : 문자
string  : 문자열
*/

public class DataType {
	public static void main(String[] args) {
		int num = 100;
		double dnum = 3.14;
		char ch = 'a';
		String name = "홍길동";
		System.out.println(num);
		System.out.println(dnum);
		System.out.println(ch);
		System.out.println(name);
	}
}




