package edu.exam09.interface01;

// VO, DTO, Model
public class PersonalNumInfo {
	private String name;
	private String number;
	
	public PersonalNumInfo(String name, String number){
		this.name = name;
		this.number = number;
	}
	public String getName() {
		return name;
	}
	public String getNumber() {
		return number;
	}
}





