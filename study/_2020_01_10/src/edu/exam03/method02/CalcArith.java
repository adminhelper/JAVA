package edu.exam03.method02;

import java.util.Scanner;

public class CalcArith {
	public static void main(String[] args) {
		int num0 = 0, num1 = 0;
		int result = 0;
		
		num0 = 10;
		num1 = 20;
		result = num0 + num1;
		System.out.printf("%d + %d의 합은 [%d]\n",
				num0, num1, result);
		
		num0 = 100;
		num1 = 200;
		result = num0 + num1;
		System.out.printf("%d + %d의 합은 [%d]\n",
				num0, num1, result);
		
		num0 = 1000;
		num1 = 2000;
		result = num0 + num1;
		System.out.printf("%d + %d의 합은 [%d]\n",
				num0, num1, result);
	}
}






