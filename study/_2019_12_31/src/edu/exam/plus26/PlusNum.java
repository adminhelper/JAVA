package edu.exam.plus26;

public class PlusNum {
	public static void main(String[] args) {
		int num = 0;
		num = num + 1;	System.out.println(num);
		num += 1;		System.out.println(num);
		num++;			System.out.println(num);
		++num;			System.out.println(num);
		
		num = num + 2;	System.out.println(num);
		num += 2;		System.out.println(num);
	}
}
